
public class prjTabuada {
	public static void main(String[] args) {
		int i = 1;
		
		while (i <= 10) {
			int j = 1;
		while (j <=10 ) {
			int produto = i * j;
            System.out.println( i + " x " + j + " = " + produto);
            
            j++;
	}
		
		// Pula uma linha para separar as tabuadas
        System.out.println();
		
		i++;
}
	
}
	
}
